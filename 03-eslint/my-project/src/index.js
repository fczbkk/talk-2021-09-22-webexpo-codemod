// basic console.log example
console.log('Hello world!')

// not a console.log
log('Hello world!')

// use different method
console.info('This is info')

// statement instead of string
console.log(40 + 2)

// multiple parameters
console.log('first', 'second', 'third')

// multi-line
console.log(
  'first',
  'second',
  'third'
)

// not a call expression
if (console.log) {
  alert('we can write to the console')
}
